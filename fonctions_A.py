def perimetre_rectangle(largeur,hauteur):
    return (largeur + hauteur)*2
print(f'Le perimetre est : {perimetre_rectangle(7,12)}')
def aire_rectangle(largeur,hauteur):
    return (largeur*hauteur)
print(f'Aire est : {aire_rectangle(7,12)}')
def perimetre_cercle(rayon):
    return 2*3.14*rayon
print(f'Le perimetre du cercle est : {perimetre_cercle(5): .2f}')
def aire_du_cercle(rayon):
    return 3.14*rayon*rayon
print(f'Aire du cercle est : {aire_du_cercle(5)}')